    private void btnloginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnloginActionPerformed
        try {
            String username = txtname.getText();
            String password = txtpass.getText();

            String sql = "SELECT id, password FROM users WHERE username = ?";
            java.sql.Connection con = dbkoneksi.konfig.sambung();
            java.sql.PreparedStatement pst = con.prepareStatement(sql);
            pst.setString(1, username);
            java.sql.ResultSet rs = pst.executeQuery();

            if (rs.next()) {
                String hashedPassword = rs.getString("password");

                if (utils.CryptoUtil.verifyPassword(password, hashedPassword)) {
                    int userId = rs.getInt("id");
                    JOptionPane.showMessageDialog(this, "Berhasil Login");

                    this.setVisible(false); // tutup login
                    new NoteView(userId).setVisible(true);
                    // TODO: lanjut ke dashboard
                } else {
                    JOptionPane.showMessageDialog(this, "Password salah");
                }

            } else {
                JOptionPane.showMessageDialog(this, "Username tidak ditemukan");
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_btnloginActionPerformed

